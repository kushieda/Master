#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	int			i;
	char		*_s1;
	const char	*_s2;

	i = 0;
	_s1 = s1;
	_s2 = s2;
	while (n > 0)
	{
		_s1[i] = _s2[i];
		n--;
		i++;
	}
	return (_s1);
}
